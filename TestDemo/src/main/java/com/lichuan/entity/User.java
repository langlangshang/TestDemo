package com.lichuan.entity;

public class User {
private String name;
private String lichuan;
private String aaa;
private Integer age;
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public String getLichuan() {
	return lichuan;
}
public void setLichuan(String lichuan) {
	this.lichuan = lichuan;
}
public String getAaa() {
	return aaa;
}
public void setAaa(String aaa) {
	this.aaa = aaa;
}



public Integer getAge() {
	return age;
}

public void setAge(Integer age) {
	this.age = age;
}

}
